"""
Общее состояние приложения.

Вынесено в отдельный модуль (а не внутрь ui/), чтобы Модуль 2
(алгоритмы компьютерного зрения и аудио-анализа, следующий шаг) мог
импортировать эти настройки напрямую, без зависимости от слоя интерфейса.
"""

from dataclasses import dataclass


@dataclass
class AISettings:
    """
    Пороговые значения чувствительности для алгоритмов детекции.

    Значения от 0 до 100 (проценты). Устанавливаются пользователем через
    слайдеры в интерфейсе; читаются модулями анализа (Модуль 2) при обработке
    видео. Пока Модуль 2 не реализован, значения только сохраняются.
    """
    low_hp_sensitivity: float = 50.0
    explosion_sensitivity: float = 50.0
    audio_peak_sensitivity: float = 50.0
    eye_contact_sensitivity: float = 50.0
    killfeed_sensitivity: float = 50.0
