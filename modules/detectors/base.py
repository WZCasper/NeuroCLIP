"""
Общий тип события для всех детекторов Модуля 2.

Каждый детектор (HP, килфид, взрывы, зрительный контакт, звуковые пики,
смех) возвращает список DetectionEvent — единый формат, который Модуль 4
(монтаж) будет использовать для склейки таймкодов, независимо от того,
какой алгоритм нашёл момент.
"""

from dataclasses import dataclass, field


@dataclass
class DetectionEvent:
    """Одно обнаруженное событие-хайлайт."""

    timestamp: float          # секунды от начала видео
    source: str                # идентификатор детектора: "low_hp", "killfeed", ...
    confidence: float          # 0.0–1.0, насколько детектор уверен в срабатывании
    label: str                 # человекочитаемое описание для UI
    metadata: dict = field(default_factory=dict)
